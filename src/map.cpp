#include "demo.hpp"



/***********************************************************************
     * Map
     * constructor

***********************************************************************/
Map::Map() {
	bgTex = new sf::Texture();
	bgSpr = new sf::Sprite();

	bgTex->loadFromFile("data/background.jpg");
	bgSpr->setTexture(*bgTex);
}



/***********************************************************************
     * Map
     * destructor

***********************************************************************/
Map::~Map() {
	delete bgTex;
	delete bgSpr;
}



/***********************************************************************
     * Map
     * update

***********************************************************************/
void Map::update() {
	render();
}



/***********************************************************************
     * Map
     * render

***********************************************************************/
void Map::render() {
	app->draw(*bgSpr);
}
